package com.inext.entity.zm;

public class SesameData {

    private Integer status;
    private Integer gender;
    private String nameOnApp;
    private String idCardOnApp;
    private Integer sesameScore;

    public Integer getStatus() {
        return status;
    }

    public void setStatus(Integer status) {
        this.status = status;
    }

    public Integer getGender() {
        return gender;
    }

    public void setGender(Integer gender) {
        this.gender = gender;
    }

    public String getNameOnApp() {
        return nameOnApp;
    }

    public void setNameOnApp(String nameOnApp) {
        this.nameOnApp = nameOnApp;
    }

    public String getIdCardOnApp() {
        return idCardOnApp;
    }

    public void setIdCardOnApp(String idCardOnApp) {
        this.idCardOnApp = idCardOnApp;
    }

    public Integer getSesameScore() {
        return sesameScore;
    }

    public void setSesameScore(Integer sesameScore) {
        this.sesameScore = sesameScore;
    }
}
